def sockMerchant(n, ar):
    # Write your code here
    
    freq=[0 for i in range(max(ar)+1)]
    num_of_pairs=0
    for i in range(n):
        freq[ar[i]]+=1
        if(freq[ar[i]]%2==0):
            num_of_pairs+=1
    return num_of_pairs
        
        

